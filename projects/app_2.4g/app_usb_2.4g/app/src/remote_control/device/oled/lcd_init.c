/**
 * @brief       向液晶写寄存器命令
 * @param       reg: 要写的命令
 * @retval      无
 */
void LCD_WR_REG(uint8_t reg)
{
#if (SPI_DRIVER)
    // Send 1-byte command (D/C low).
    s_lcd_u8 = reg;
    LCD_TX_Begin(LCD_TX_CMD);
    LCD_TX_Write_DMA_Async(&s_lcd_u8, 1, NULL);
    LCD_TX_End();      // CS deassert happens in the DMA/SPI completion ISR
    LCD_TX_WaitDone();

    // Keep default state as DATA (matches old behavior).
    LCD_DC_Set();
#else
    LCD_DC_Clr();
    LCD_WR_Bus(reg);
    LCD_DC_Set();
#endif
}

/**
 * @brief       向液晶写一个8bit数据
 * @param       dat: 要写的数据
 * @retval      无
 */
void LCD_WR_DATA8(uint8_t dat)
{
#if (SPI_DRIVER) //硬件SPI
    // Send 1-byte data (D/C high).
    s_lcd_u8 = dat;
    LCD_TX_Begin(LCD_TX_DATA);
    LCD_TX_Write_DMA_Async(&s_lcd_u8, 1, NULL);
    LCD_TX_End();      // CS deassert happens in the DMA/SPI completion ISR
    LCD_TX_WaitDone();
#else //软件SPI
    LCD_DC_Set();
    LCD_WR_Bus(dat);
#endif
}

/**
 * @brief       向液晶写一个16bit数据
 * @param       dat: 要写的数据
 * @retval      无
 */
void LCD_WR_DATA(uint16_t dat)
{
    // Send 16-bit data (D/C high).
    LCD_WR_Bus_16(dat);
}


