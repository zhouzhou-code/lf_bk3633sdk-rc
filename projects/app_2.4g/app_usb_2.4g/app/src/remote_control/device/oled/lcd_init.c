/**
 * @brief       设置显示窗口
 * @param       xs:窗口列起始地址
 * @param       ys:坐标行起始地址
 * @param       xe:窗口列结束地址
 * @param       ye:坐标行结束地址
 * @retval      无
 * @note        由于IC限制;列地址参数必须为4的倍数
 */
void LCD_Address_Set(uint16_t xs, uint16_t ys, uint16_t xe, uint16_t ye)
{

    LCD_WR_REG(0x2A); // 列地址设置
    LCD_WR_DATA8(xs >> 8);
    LCD_WR_DATA8(xs & 0xFF);
    LCD_WR_DATA8(xe >> 8);
    LCD_WR_DATA8(xe & 0xFF);
    LCD_WR_REG(0x2B); // 行地址设置
    LCD_WR_DATA8(ys >> 8);
    LCD_WR_DATA8(ys & 0xFF);
    LCD_WR_DATA8(ye >> 8);
    LCD_WR_DATA8(ye & 0xFF);
    LCD_WR_REG(0x2c); // 储存器写
    // Delay_ms(1);
    Delay_us(5);
    LCD_WR_REG(0x2c);
}

/**
 * @brief       指定颜色填充区域
 * @param       xs:填充区域列起始地址
 * @param       ys:填充区域行起始地址
 * @param       xe:填充区域列结束地址
 * @param       ye:填充区域行结束地址
 * @param       color:填充颜色值
 * @retval      无
 */
void LCD_Fill(uint16_t xs, uint16_t ys, uint16_t xe, uint16_t ye, uint16_t color)
{
    uint16_t i, j;
    LCD_Address_Set(xs, ys, xe - 1, ye - 1);
    for (j = ys; j < ye; j++)
    {
        for (i = xs; i < xe; i++)
        {
            LCD_WR_DATA(color);
        }
    }
}

